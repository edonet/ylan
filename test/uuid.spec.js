import uuid from '../lib/uuid';


/**
 *****************************************
 * 测试模块
 *****************************************
 */
describe('测试：uuid', () => {
    test('uuid', () => {
        let count = 1000,
            map = {};

        // 校验生成唯一【id】
        while (count --) {
            let id = uuid();

            expect(map[id]).toBeFalsy();
            map[id] = true;
        }
    });
});
