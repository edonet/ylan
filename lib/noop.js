'use strict';


/**
 *****************************************
 * 空函数
 *****************************************
 */
function noop() {}


/**
 *****************************************
 * 真函数
 *****************************************
 */
function truthy() {
    return true;
}


/**
 *****************************************
 * 假函数
 *****************************************
 */
function falsy() {
    return false;
}


/**
 *****************************************
 * 第一参数
 *****************************************
 */
function argv(data) {
    return data;
}


/**
 *****************************************
 * 参数列表
 *****************************************
 */
function args(...list) {
    return list;
}


/**
 *****************************************
 * 抛出接口
 *****************************************
 */
export default noop;
export { truthy, falsy, argv, args };
